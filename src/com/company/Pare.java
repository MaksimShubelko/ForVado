package com.company;

public class Pare {
    private double sqr;
    private double lamda;

    public double getSqr() {
        return sqr;
    }

    public double getLamda() {
        return lamda;
    }

    Pare(double sqr, double lamda) {
        this.sqr = sqr;
        this.lamda = lamda;
    }
}
